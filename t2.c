#include <stdio.h>

int main()
{

    const int int_const=5;
    const char char_const='A';
    const float float_const=111.44;
    printf("Printing value of integer const: %d\n ",int_const);
    printf("Printing value of character const: %c\n ",char_const);
    printf("Printing value of float const: %0.2f\n ",float_const);

    return 0;
}